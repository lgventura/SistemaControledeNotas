    private void btnCadastrarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarProdutoActionPerformed
        String valorUni = "";

        if(txtNomeProd.getText().equals("") || txtQntd.getText().equals("") || txtValor_prod.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha Corretamente os Campos !!", "Atenção", ERROR_MESSAGE);
        }else{

            produto.setNota_entrada_fk();
            produto.setNome(txtNomeProd.getText());
            produto.setTpProdutos_cod(comboTIpoProd.getSelectedItem().toString());
            produto.setUnidade(selectTpUnidade.getSelectedItem().toString());
            produto.setQntd(Integer.parseInt(txtQntd.getText()));

            valorUni = txtValor_prod.getText().replace(".", "").replace(",", ".");
            produto.setValor_uni(Double.parseDouble(valorUni));
            produto.setValor_total();
            produto.setDescricao(txtAreaDesc.getText());

            if(produto.cadastrar()){
                JOptionPane.showMessageDialog(null, "Produto "+txtNomeProd.getText()+" Adicionado a Nota:"+txtNNota.getText());
                atualizaTabelaProdutos();
                limparCamposTelaProduto();
            }else {
                JOptionPane.showMessageDialog(null, "Erro ao adicionar produto, contato o suporte do sistema", "Atenção", ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnCadastrarProdutoActionPerformed

