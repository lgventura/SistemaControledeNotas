    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String vNota = "", vAlt = "";
        
        if(txtCodForn.getText().equals("") || txtSerie.getText().equals("") || txtNNota.getText().equals("") || txtEmpenho.getText().equals("") || dataEmpenho.getDateFormatString().equals("") || dataEmissao.getDateFormatString().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha Corretamente os Campos !!", "Atenção", ERROR_MESSAGE);
        }else {
            nota.setFornecedor_cod(Integer.parseInt(txtCodForn.getText()));
            nota.setSecretaria_cod(lblDep.getText());
            nota.setExercicio(Integer.parseInt(lblEx.getText()));
            nota.setSituacao(1);
            nota.setAno(anoChosser.getYear());
            nota.setSerie(Integer.parseInt(txtSerie.getText()));
            nota.setN_nota(Integer.parseInt(txtNNota.getText()));
            nota.setEmpenho(Integer.parseInt(txtEmpenho.getText()));
            nota.setData_empenho(new Date(dataEmpenho.getDate().getTime()));
            nota.setData_entrada(produto.getDateTime());
            nota.setTp_doc(comboTpDoc.getSelectedItem().toString());
            nota.setTp_nota(comboTpNota.getSelectedItem().toString());
            nota.setData_emissao(new Date(dataEmissao.getDate().getTime()));
            nota.setDescricao(txtAreaObs.getText());
            nota.setTp_entrada(comboTipoEntrada.getSelectedItem().toString());
            nota.setAlteracao_valor(comboAlt.getSelectedItem().toString());


            vNota = txtValor_Nota.getText().replace(".", "").replace(",", ".");
            nota.setValor_nota(Double.parseDouble(vNota));

            vAlt = txtValor_alteracao.getText().replace(".", "").replace(",", ".");
            nota.setValor_alteracao(Double.parseDouble(vAlt));
        
            if(nota.cadastrar()){
                
                JOptionPane.showMessageDialog(null, "Adicionar os Produtos !!");
                addProdutos.setEnabled(true);
                addProdutos.setVisible(true);

            }else {
                JOptionPane.showMessageDialog(null, "Erro ao adicionar Nota", "erro", ERROR_MESSAGE);
            }  
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

