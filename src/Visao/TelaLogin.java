    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed
        if(userLogin.getText().equals("") && senhaLogin.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha os campos de Usu치rio e Senha para continuar!!");
        }
        else if(userLogin.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo Usu치rio!!");
        }else if(senhaLogin.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo Senha!!");
        }else {
        
            logar.setUsuario(userLogin.getText());
            logar.setSenha(senhaLogin.getText());

            if(logar.verificaLogin()){
                JOptionPane.showMessageDialog(null, "Login efetuado com sucesso");
                iniciar.setEnabled(true);
                iniciar.setVisible(true);
                this.setEnabled(false);
                this.setVisible(false);
                this.dispose();
            }else {
                JOptionPane.showMessageDialog(null, "Usu치rio ou Senha invalidos\n\tPor favor tente novamente");
            }
        }
    }//GEN-LAST:event_btnLogarActionPerformed

